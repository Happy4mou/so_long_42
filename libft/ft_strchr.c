#include "libft.h"

int	ft_strchr(char *s)
{
	int	i;

	i = 0;
	while (s[i])
	{
		if (s[i] != '1')
		{
			printf("----gggg\n");
			return (0);
		}
		i++;
	}
	return (1);
}
